def check_month(strMonth):
    if (strMonth == "januari"):
        return '01'
    if (strMonth == "februari"):
        return '02'
    if (strMonth == "maret"):
        return '03'
    if (strMonth == "april"):
        return '04'
    if (strMonth == "mei"):
        return '05'
    if (strMonth == "juni"):
        return '06'
    if (strMonth == "juli"):
        return '07'
    if (strMonth == "agustus"):
        return '08'
    if (strMonth == "september"):
        return '09'
    if (strMonth == "oktober"):
        return '10'
    if (strMonth == "november"):
        return '11'
    if (strMonth == "desember"):
        return '12'